package vo;

public class NoticeVO {
	private String ntcNum;
	private String ntcSubject;
	private String ntcMemid;
	private String ntcDate;
	private String ntcContent;
	private String ntcView;
	
	
	
	
	
	public String getNtcNum() {
		return ntcNum;
	}
	public void setNtcNum(String ntcNum) {
		this.ntcNum = ntcNum;
	}
	public String getNtcSubject() {
		return ntcSubject;
	}
	public void setNtcSubject(String ntcSubject) {
		this.ntcSubject = ntcSubject;
	}
	public String getNtcMemid() {
		return ntcMemid;
	}
	public void setNtcMemid(String ntcMemid) {
		this.ntcMemid = ntcMemid;
	}
	public String getNtcDate() {
		return ntcDate;
	}
	public void setNtcDate(String ntcDate) {
		this.ntcDate = ntcDate;
	}
	public String getNtcContent() {
		return ntcContent;
	}
	public void setNtcContent(String ntcContent) {
		this.ntcContent = ntcContent;
	}
	public String getNtcView() {
		return ntcView;
	}
	public void setNtcView(String ntcView) {
		this.ntcView = ntcView;
	}
	
	
	
	
	
	
	
	
}
