package vo;

public class FreeBoardVO {
	private String fbNum;
	private String fbSubject;
	private String fbMemid;
	private String fbDate;
	private String fbContent;
	private String fbView;
	
	
	
	
	public String getFbNum() {
		return fbNum;
	}
	public void setFbNum(String fbNum) {
		this.fbNum = fbNum;
	}
	public String getFbSubject() {
		return fbSubject;
	}
	public void setFbSubject(String fbSubject) {
		this.fbSubject = fbSubject;
	}
	public String getFbMemid() {
		return fbMemid;
	}
	public void setFbMemid(String fbMemid) {
		this.fbMemid = fbMemid;
	}
	public String getFbDate() {
		return fbDate;
	}
	public void setFbDate(String fbDate) {
		this.fbDate = fbDate;
	}
	public String getFbContent() {
		return fbContent;
	}
	public void setFbContent(String fbContent) {
		this.fbContent = fbContent;
	}
	public String getFbView() {
		return fbView;
	}
	public void setFbView(String fbView) {
		this.fbView = fbView;
	}
	
	
	
	
}
